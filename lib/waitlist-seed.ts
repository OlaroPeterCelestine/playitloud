function emailToId(email: string) {
  return email.trim().toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "")
}

export async function seedWaitlist(db: Firestore) {
  const now = new Date().toISOString()
  for (const entry of WAITLIST_SEED) {
    const id = emailToId(entry.email)
    await setDoc(
      doc(db, "waitlist", id),
      {
        name: entry.name,
        email: entry.email,
        joinedAt: now,
        status: "pending",
      },
      { merge: true }
    )
  }
}


